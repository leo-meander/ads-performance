def _sunsetting_specs(cfg: dict[str, Any]) -> list[RuleSpec]:
    """Phase 2: Multi-step under-performer wind-down.

    The state machine lives in the rule engine, but each step still needs a
    rule row so logs/UI surface what's happening. Steps are:
      1. Reduce budget by `step1_reduction_pct` (default 25%)
      2. Reduce budget by `step2_reduction_pct` (default 50%, cumulative from original)
      3. Pause permanently

    Step progression is tracked via action_logs queried for the same ad_set.
    See rule_engine.sunsetting_step_for_entity().
    """
    return [
        RuleSpec(
            name_suffix="sunsetting",
            entity_level="ad_set",
            # `adjust_budget` is the entry action; the engine substitutes pause_adset on step 3.
            action="adjust_budget",
            conditions=[
                {"metric": "roas", "operator": "<", "threshold": cfg["roas_min"], "days": cfg["days"]},
                {"metric": "spend", "operator": ">=", "threshold": cfg["spend_min"], "days": cfg["days"]},
            ],
            action_params={
                "sunsetting": True,
                "step1_reduction_pct": cfg["step1_reduction_pct"],
                "step2_reduction_pct": cfg["step2_reduction_pct"],
            },
        ),
    ]


def _scale_winning_adset_specs(cfg: dict[str, Any]) -> list[RuleSpec]:
    """Phase 2: Sustainable +N%/day budget scaling, capped at max multiplier.

    Differs from SURF: SURF reverts next day; Scale Winning ratchets up gradually
    and keeps the gain (until momentum fades and stop_loss/sunsetting takes over).
    """
    return [
        RuleSpec(
            name_suffix="scale_step",
            entity_level="ad_set",
            action="adjust_budget",
            conditions=[
                {"metric": "roas", "operator": ">=", "threshold": cfg["roas_min"], "days": cfg["days"]},
                {"metric": "spend", "operator": ">=", "threshold": cfg["spend_min"], "days": cfg["days"]},
            ],
            action_params={
                "scale_winning": True,
                "daily_step_pct": cfg["daily_step_pct"],
                "max_budget_cap_multiplier": cfg["max_budget_cap_multiplier"],
            },
        ),
    ]


def _custom_rule_specs(cfg: dict[str, Any]) -> list[RuleSpec]:
    """Custom-rule preset: a single rule built directly from user config.

    Replaces the legacy /rules UI — users pick entity_level + conditions +
    action, no preset constraint. No revert, no sunsetting, no step machine.
    Fires once per day with the rest of the tactic cohort (17:00 UTC).
    """
    return [
        RuleSpec(
            name_suffix="custom",
            entity_level=cfg.get("entity_level", "ad"),
            action=cfg.get("action", "send_alert"),
            conditions=cfg.get("conditions") or [],
            action_params=cfg.get("action_params") or None,
        ),
    ]


