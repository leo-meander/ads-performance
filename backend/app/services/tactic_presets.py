"""Tactic preset library.

Each preset describes a Madgicx-style strategy as:
- default config (thresholds, multipliers, caps) — overridable per-tactic
- rule_specs: how to materialize the tactic into AutomationRule rows
- revert_policy: what (if anything) the daily cron should undo next day

The rule_engine reads `tactic.config` at evaluation time so most overrides
take effect without re-creating rules.
"""

from dataclasses import dataclass, field
from typing import Any, Callable

from app.models.tactic import (
    PRESET_CUSTOM_RULE,
    PRESET_PAUSE_PERMANENT,
    PRESET_PAUSE_TODAY,
    PRESET_REVIVE_AD,
    PRESET_REVIVE_ADSET,
    PRESET_SCALE_WINNING_ADSET,
    PRESET_SCALE_WINNING_CAMPAIGN,
    PRESET_STOP_LOSS_AD,
    PRESET_STOP_LOSS_ADSET,
    PRESET_SUNSETTING,
    PRESET_SURF_ADSET,
    PRESET_SURF_CAMPAIGN,
)

# Revert policies — drives the daily-cron revert phase.
REVERT_NONE = "none"               # Permanent action; never auto-undo
REVERT_NEXT_DAY = "next_day"       # Undo at next daily run (Pause Today, SURF)
REVERT_ON_RECOVERY = "on_recovery" # Re-evaluate next day; tactic decides via REVIVE rule


@dataclass
class RuleSpec:
    """Spec for one AutomationRule generated by a preset.

    `conditions` and `action_params` may include `${...}` template tokens
    referencing keys in `tactic.config` — resolved at rule materialization time.
    """

    name_suffix: str
    entity_level: str  # campaign | ad_set | ad
    action: str        # pause_campaign | pause_adset | pause_ad | enable_* | adjust_budget | send_alert
    conditions: list[dict[str, Any]]
    action_params: dict[str, Any] | None = None


@dataclass
class TacticPreset:
    name: str
    description: str
    default_config: dict[str, Any]
    rule_specs_fn: Callable[[dict[str, Any]], list[RuleSpec]]
    revert_policy: str = REVERT_NONE
    valid_platforms: list[str] = field(default_factory=lambda: ["meta"])


