def get_preset(preset_type: str) -> TacticPreset:
    if preset_type not in PRESETS:
        raise ValueError(f"Unknown preset_type: {preset_type}. Valid: {sorted(PRESETS.keys())}")
    return PRESETS[preset_type]


def list_presets() -> list[dict[str, Any]]:
    """For UI dropdowns. Returns minimal shape — fetch full preset via get_preset."""
    return [
        {
            "preset_type": k,
            "name": p.name,
            "description": p.description,
            "default_config": p.default_config,
            "revert_policy": p.revert_policy,
            "valid_platforms": p.valid_platforms,
        }
        for k, p in PRESETS.items()
    ]
